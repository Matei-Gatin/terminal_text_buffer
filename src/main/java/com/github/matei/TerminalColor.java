package com.github.matei;

/**
 * Standard terminal colors.
 * <p>
 * Includes the 16 ANSI colors (8 normal + 8 bright) and a
 * {@link #DEFAULT} value representing the terminal's default color.
 */
public enum TerminalColor {
    DEFAULT,

    BLACK,
    RED,
    GREEN,
    YELLOW,
    BLUE,
    MAGENTA,
    CYAN,
    WHITE,

    BRIGHT_BLACK,
    BRIGHT_RED,
    BRIGHT_GREEN,
    BRIGHT_YELLOW,
    BRIGHT_BLUE,
    BRIGHT_MAGENTA,
    BRIGHT_CYAN,
    BRIGHT_WHITE;
}
